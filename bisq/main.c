#include "head.h"

int		main(int ac, char **av)
{
	int		i;

	i = 1;
	while (i < ac)
	{
		if (checkfn(av[i]) == 1)
			return (0);
		write(1, "\n", 1);
		if (i != ac - 1)
			write(1, "\n", 1);
		i++;
	}
	if (ac == 1)
	{
		checkfn(0);
		write(1, "\n", 1);
	}
	return (0);
}
