int		plus(int a, int b)
{
	return (a + b);
}

int		minus(int a, int b)
{
	return (a - b);
}

int		divide(int a, int b)
{
	return (a / b);
}

int		umn(int a, int b)
{
	return (a * b);
}

int		mod(int a, int b)
{
	return (a % b);
}
